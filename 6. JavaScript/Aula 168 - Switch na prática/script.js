

var escolha = prompt("Digite um numero: ");

switch (escolha) {

    case "1":
        document.write("Você digitou 1");
        break; 

    case '2':
        document.write("Você digitou 2");
        break; 
        
    case '3':
        document.write("Você digitou 3");
        break; 

    default: 
        document.write("Default");
        break;
}
